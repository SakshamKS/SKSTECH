public class Ternary {
    public static void main(String[] args) {
        int a = 13;
        int b = 1;
        int min;
        System.out.println("first number" + a );
        System.out.println("Second number" + b);
        //Ternary operator
        min = (a < b) ? a : b;

        System.out.println("Minimum is =" + " " + min);
    }
}
